package com.kodilla.basic_assertion;

public class Calculator {

        public int sum(int a, int b) { // metoda sumujaca dwie zmienne
            return a + b;

        }

        public int subtract(int a, int b) { // metoda wykonujaca operacje odejmowania
            return a - b;
        }

        public double square(double c, double d) { // metoda podnoszaca zmienna a do kwadratu
            return Math.pow(c, d);
        }
    }



package com.kodilla.basic_assertion;

public class ResultChecker {
    public static boolean assertEquals(int expected, int actual) { // metoda statyczna assertEquals, dwie zmienne. Zwraca wynik porownania wyliczone do oczekiwane
        return expected == actual;
    }
    public static boolean assertEqualsSecond(int expected, int actual) { // metoda statyczna assertEquals, dwie zmienne. Zwraca wynik porownania wyliczone do oczekiwane
        return expected == actual;
    }

    public static boolean assertEqualsSquare(double expected, double actual) { // metoda statyczna assertEqualsSquare, dwie zmienne. Zwraca wynik porownania wyliczone do oczekiwane
        return expected == actual;
    }
}
